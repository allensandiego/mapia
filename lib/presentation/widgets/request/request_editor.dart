  Future<void> _fetchToken(BuildContext context, WidgetRef ref) async {
    final req = widget.tab.request;
    final auth = req.auth;
    final oauth2 = auth.oauth2Config;
    final notifier = ref.read(tabsProvider.notifier);

    if (oauth2.tokenUrl.isEmpty) {
      ScaffoldMessenger.of(context).showSnackBar(
        const SnackBar(content: Text('Token URL is required')),
      );
      return;
    }

    try {
      final Map<String, dynamic> body = {
        'grant_type': _grantType(oauth2.flow),
        'client_id': oauth2.clientId,
      };

      if (oauth2.clientSecret.isNotEmpty) {
        body['client_secret'] = oauth2.clientSecret;
      }

      switch (oauth2.flow) {
        case OAuth2Flow.clientCredentials:
          body['grant_type'] = 'client_credentials';
          break;
        case OAuth2Flow.password:
          body['grant_type'] = 'password';
          body['username'] = oauth2.username;
          body['password'] = oauth2.password;
          break;
        case OAuth2Flow.authorizationCode:
          ScaffoldMessenger.of(context).showSnackBar(
            const SnackBar(
                content: Text(
                    'Authorization Code flow requires browser interaction')),
          );
          return;
        case OAuth2Flow.implicit:
          ScaffoldMessenger.of(context).showSnackBar(
            const SnackBar(content: Text('Implicit flow not supported')),
          );
          return;
      }

      if (oauth2.scope.isNotEmpty) {
        body['scope'] = oauth2.scope;
      }

      final httpService = HttpService();
      final tokenResponse = await httpService.fetchOAuth2Token(
        oauth2.tokenUrl,
        body,
      );

      if (tokenResponse != null) {
        final accessToken = tokenResponse['access_token'] ?? '';
        final expiresIn = tokenResponse['expires_in'] ?? 0;
        final refreshToken = tokenResponse['refresh_token'] ?? '';

        final tokenExpiry = expiresIn > 0
            ? DateTime.now().add(Duration(seconds: expiresIn as int))
            : null;

        if (!mounted) return;

        notifier.updateRequest(
          widget.tab.id,
          req.copyWith(
            auth: auth.copyWith(
              oauth2Config: oauth2.copyWith(
                accessToken: accessToken,
                expiresIn: expiresIn,
                refreshToken: refreshToken,
                tokenExpiry: tokenExpiry,
              ),
            ),
          ),
        );

        if (!mounted) return;
        ScaffoldMessenger.of(context).showSnackBar(
          const SnackBar(content: Text('Token fetched successfully')),
        );
      } else {
        if (!mounted) return;
        ScaffoldMessenger.of(context).showSnackBar(
          const SnackBar(content: Text('Failed to fetch token')),
        );
      }
    } catch (e) {
      if (!mounted) return;
      ScaffoldMessenger.of(context).showSnackBar(
        SnackBar(content: Text('Error: $e')),
      );
    }
  }

  String _grantType(OAuth2Flow flow) {
    switch (flow) {
      case OAuth2Flow.clientCredentials:
        return 'client_credentials';
      case OAuth2Flow.password:
        return 'password';
      case OAuth2Flow.authorizationCode:
        return 'authorization_code';
      case OAuth2Flow.implicit:
        return 'implicit';
    }
  }
