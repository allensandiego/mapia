class _AuthField extends StatefulWidget {
  final String label;
  final String value;
  final String hint;
  final bool obscure;
  final ValueChanged<String> onChanged;
  final List<String>? envVars;
  final Map<String, String>? resolvedEnvVars;

  const _AuthField({
    required this.label,
    required this.value,
    required this.hint,
    required this.onChanged,
    this.obscure = false,
    this.envVars,
    this.resolvedEnvVars,
  });

  @override
  State<_AuthField> createState() => _AuthFieldState();
}

class _AuthFieldState extends State<_AuthField> {
  late VariableTextEditingController _ctrl;
  final FocusNode _focus = FocusNode();

  @override
  void initState() {
    super.initState();
    _ctrl = VariableTextEditingController(text: widget.value);
  }

  @override
  void dispose() {
    _ctrl.dispose();
    _focus.dispose();
    super.dispose();
  }

  @override
  Widget build(BuildContext context) {
    _ctrl.variableColor = context.colors.warning;
    _ctrl.defaultColor = context.colors.textPrimary;

    final text = _ctrl.text;
    final hasVars = text.contains('{{');
    final resolvedText = (hasVars && widget.resolvedEnvVars != null)
        ? VariableParser.resolve(text, widget.resolvedEnvVars!)
        : null;

    Widget field = VariableAutocompleteField(
      controller: _ctrl,
      focusNode: _focus,
      obscureText: widget.obscure,
      envVars: widget.envVars ?? [],
      resolvedEnvVars: widget.resolvedEnvVars,
      onChanged: widget.onChanged,
      style: TextStyle(color: context.colors.textPrimary, fontSize: 14),
      decoration: InputDecoration(
        hintText: widget.hint,
        border: const OutlineInputBorder(),
      ),
    );

    if (resolvedText != null && resolvedText != text) {
      field = Tooltip(
        message: resolvedText,
        preferBelow: true,
        decoration: BoxDecoration(
          color: context.colors.bgElevated,
          borderRadius: BorderRadius.circular(6),
          border:
              Border.all(color: context.colors.warning.withValues(alpha: 0.4)),
        ),
        textStyle: TextStyle(
          fontSize: 12,
          color: context.colors.warning,
          fontFamily: 'monospace',
        ),
        child: field,
      );
    }

    return Column(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: [
        Text(widget.label,
            style: TextStyle(
                color: context.colors.textPrimary,
                fontWeight: FontWeight.bold,
                fontSize: 14)),
        const SizedBox(height: 8),
        field,
      ],
    );
  }
}

