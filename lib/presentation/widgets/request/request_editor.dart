class RequestEditor extends ConsumerStatefulWidget {
  final RequestTab tab;
  const RequestEditor({super.key, required this.tab});

  @override
  ConsumerState<RequestEditor> createState() => _RequestEditorState();
}

class _RequestEditorState extends ConsumerState<RequestEditor>
    with SingleTickerProviderStateMixin {
  late TabController _tabCtrl;

  @override
  void initState() {
    super.initState();
    _tabCtrl = TabController(length: 4, vsync: this);
  }

  @override
  void dispose() {
    _tabCtrl.dispose();
    super.dispose();
  }

  @override
  Widget build(BuildContext context) {
    final req = widget.tab.request;
    final notifier = ref.read(tabsProvider.notifier);

    // Collect available environment variables
    final activeEnvVars = ref.watch(activeEnvVariablesProvider);
    final allVars = <String>{...activeEnvVars.keys};
    final resolvedVars = Map<String, String>.from(activeEnvVars);
    if (widget.tab.collectionId != null) {
      final collections = ref.watch(collectionsProvider).value ?? [];
      final col =
          collections.where((c) => c.id == widget.tab.collectionId).firstOrNull;
      if (col != null) {
        if (col.environmentId != null) {
          final envs = ref.watch(environmentsProvider).value ?? [];
          final linkedEnv =
              envs.where((e) => e.id == col.environmentId).firstOrNull;
          if (linkedEnv != null) {
            for (final v in linkedEnv.variables.where((v) => v.enabled)) {
              allVars.add(v.key);
              resolvedVars.putIfAbsent(v.key, () => v.value);
            }
          }
        }
        for (final v in col.variables.where((v) => v.enabled)) {
          allVars.add(v.key);
          resolvedVars.putIfAbsent(v.key, () => v.value);
        }
      }
    }
    for (final v in req.variables.where((v) => v.enabled)) {
      allVars.add(v.key);
      resolvedVars.putIfAbsent(v.key, () => v.value);
    }
    final envVarsList = allVars.toList();

    return Column(
      children: [
        // Sub-tab bar
        Container(
          color: context.colors.bgSurface,
          child: TabBar(
            controller: _tabCtrl,
            isScrollable: true,
            tabAlignment: TabAlignment.start,
            labelStyle:
                const TextStyle(fontSize: 12, fontWeight: FontWeight.w500),
            tabs: const [
              Tab(text: 'Authorization'),
              Tab(text: 'Headers'),
              Tab(text: 'Params'),
              Tab(text: 'Body'),
            ],
          ),
        ),
        const Divider(height: 1),
        // Tab content
        Expanded(
          child: TabBarView(
            controller: _tabCtrl,
            children: [
              // Auth
              _AuthEditor(tab: widget.tab),
              // Headers
              SingleChildScrollView(
                child: Column(
                  children: [
                    KeyValueEditor(
                      items: req.headers,
                      keyHint: 'Header',
                      valueHint: 'Value',
                      envVars: envVarsList,
                      resolvedEnvVars: resolvedVars,
                      onChanged: (v) => notifier.updateRequest(
                        widget.tab.id,
                        req.copyWith(headers: v),
                      ),
                    ),
                    _AutoGeneratedHeaders(request: req),
                  ],
                ),
              ),
              // Params
              SingleChildScrollView(
                child: KeyValueEditor(
                  items: req.queryParams,
                  keyHint: 'Key',
                  valueHint: 'Value',
                  envVars: envVarsList,
                  resolvedEnvVars: resolvedVars,
                  onChanged: (v) => notifier.updateRequest(
                    widget.tab.id,
                    req.copyWith(queryParams: v),
                  ),
                ),
              ),
              // Body
              _BodyEditor(tab: widget.tab),
            ],
          ),
        ),
      ],
    );
  }
}

