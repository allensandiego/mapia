class _BodyEditor extends ConsumerWidget {
  final RequestTab tab;
  const _BodyEditor({required this.tab});

  @override
  Widget build(BuildContext context, WidgetRef ref) {
    final req = tab.request;
    final notifier = ref.read(tabsProvider.notifier);

    // Collect available environment variables
    final activeEnvVars = ref.watch(activeEnvVariablesProvider);
    final allVars = <String>{...activeEnvVars.keys};
    final resolvedVars = Map<String, String>.from(activeEnvVars);
    if (tab.collectionId != null) {
      final collections = ref.watch(collectionsProvider).value ?? [];
      final col =
          collections.where((c) => c.id == tab.collectionId).firstOrNull;
      if (col != null) {
        if (col.environmentId != null) {
          final envs = ref.watch(environmentsProvider).value ?? [];
          final linkedEnv =
              envs.where((e) => e.id == col.environmentId).firstOrNull;
          if (linkedEnv != null) {
            for (final v in linkedEnv.variables.where((v) => v.enabled)) {
              allVars.add(v.key);
              resolvedVars.putIfAbsent(v.key, () => v.value);
            }
          }
        }
        for (final v in col.variables.where((v) => v.enabled)) {
          allVars.add(v.key);
          resolvedVars.putIfAbsent(v.key, () => v.value);
        }
      }
    }
    for (final v in req.variables.where((v) => v.enabled)) {
      allVars.add(v.key);
      resolvedVars.putIfAbsent(v.key, () => v.value);
    }
    final envVarsList = allVars.toList();

    return Column(
      children: [
        // Editor area — the body-type dropdown is embedded inside each sub-editor's toolbar
        Expanded(
            child: _buildBodyContent(
                context, req, notifier, envVarsList, resolvedVars)),
      ],
    );
  }

  Widget _buildBodyContent(BuildContext context, ApiRequest request, notifier,
      List<String> envVarsList, Map<String, String> resolvedVars) {
    switch (request.bodyType) {
      case BodyType.none:
        return Column(
          children: [
            _BodyTypeBar(tab: tab),
            const Divider(height: 1),
            Expanded(
              child: Center(
                child: Text('No body for this request.',
                    style: TextStyle(
                        fontSize: 12, color: context.colors.textMuted)),
              ),
            ),
          ],
        );
      case BodyType.formData:
      case BodyType.urlEncoded:
        return Column(
          children: [
            _BodyTypeBar(tab: tab),
            const Divider(height: 1),
            Expanded(
              child: SingleChildScrollView(
                child: KeyValueEditor(
                  items: request.formDataFields,
                  keyHint: 'Key',
                  valueHint: 'Value',
                  envVars: envVarsList,
                  resolvedEnvVars: resolvedVars,
                  onChanged: (v) => notifier.updateRequest(
                    tab.id,
                    request.copyWith(formDataFields: v),
                  ),
                ),
              ),
            ),
          ],
        );
      case BodyType.json:
      case BodyType.raw:
      case BodyType.binary:
        return _RawBodyEditor(
          tab: tab,
          resolvedEnvVars: resolvedVars,
          envVars: envVarsList,
        );
    }
  }
}

// ─── Body Type Bar (Row of ChoiceChips) ──────────────────────────────────────

class _BodyTypeBar extends ConsumerWidget {
  final RequestTab tab;
  const _BodyTypeBar({required this.tab});

  String _label(BodyType t) {
    switch (t) {
      case BodyType.none:
        return 'none';
      case BodyType.json:
        return 'JSON';
      case BodyType.formData:
        return 'form-data';
      case BodyType.urlEncoded:
        return 'x-www-form-urlencoded';
      case BodyType.raw:
        return 'raw';
      case BodyType.binary:
        return 'binary';
    }
  }

  @override
  Widget build(BuildContext context, WidgetRef ref) {
    final req = tab.request;
    final notifier = ref.read(tabsProvider.notifier);
    return Container(
      color: context.colors.bgSurface,
      padding: const EdgeInsets.all(8),
      child: SingleChildScrollView(
        scrollDirection: Axis.horizontal,
        child: Row(
          children: BodyType.values.map((t) {
            return Padding(
              padding: const EdgeInsets.only(right: 8),
              child: ChoiceChip(
                label: Text(_label(t)),
                selected: req.bodyType == t,
                onSelected: (_) {
                  notifier.updateRequest(tab.id, req.copyWith(bodyType: t));
                },
              ),
            );
          }).toList(),
        ),
      ),
    );
  }
}

class _RawBodyEditor extends ConsumerStatefulWidget {
  final RequestTab tab;
  final Map<String, String>? resolvedEnvVars;
  final List<String>? envVars;
  const _RawBodyEditor({required this.tab, this.resolvedEnvVars, this.envVars});

  @override
  ConsumerState<_RawBodyEditor> createState() => _RawBodyEditorState();
}

class _RawBodyEditorState extends ConsumerState<_RawBodyEditor>
    with SingleTickerProviderStateMixin {
  late VariableTextEditingController _ctrl;
  late TabController _tabCtrl;
  final FocusNode _focus = FocusNode();

  @override
  void initState() {
    super.initState();
    _ctrl = VariableTextEditingController(text: widget.tab.request.body);
    _tabCtrl = TabController(length: 2, vsync: this);
  }

  @override
  void didUpdateWidget(_RawBodyEditor old) {
    super.didUpdateWidget(old);
    if (old.tab.id != widget.tab.id) {
      _ctrl.text = widget.tab.request.body;
    }
  }

  @override
  void dispose() {
    _ctrl.dispose();
    _tabCtrl.dispose();
    _focus.dispose();
    super.dispose();
  }

  String get _language {
    switch (widget.tab.request.bodyType) {
      case BodyType.json:
        return 'json';
      case BodyType.raw:
        return 'plaintext';
      default:
        return 'plaintext';
    }
  }

  String _bodyTypeLabel(BodyType t) {
    switch (t) {
      case BodyType.none:
        return 'none';
      case BodyType.json:
        return 'JSON';
      case BodyType.formData:
        return 'form-data';
      case BodyType.urlEncoded:
        return 'x-www-form-urlencoded';
      case BodyType.raw:
        return 'raw';
      case BodyType.binary:
        return 'binary';
    }
  }

  @override
  Widget build(BuildContext context) {
    final req = widget.tab.request;

    _ctrl.variableColor = context.colors.warning;
    _ctrl.defaultColor = context.colors.textPrimary;

    return Column(
      children: [
        // Combined: Edit/Preview tabs (left) + Body type dropdown (right)
        Container(
          height: 36,
          color: context.colors.bgSurface,
          child: Row(
            children: [
              // Edit / Preview tabs take up the remaining space
              Expanded(
                child: TabBar(
                  controller: _tabCtrl,
                  isScrollable: true,
                  tabAlignment: TabAlignment.start,
                  labelStyle: const TextStyle(
                      fontSize: 11, fontWeight: FontWeight.w500),
                  tabs: const [
                    Tab(text: 'Edit'),
                    Tab(text: 'Preview'),
                  ],
                ),
              ),
              // Body type dropdown — right-aligned
              Padding(
                padding: const EdgeInsets.only(right: 8),
                child: DropdownButtonHideUnderline(
                  child: DropdownButton<BodyType>(
                    value: req.bodyType,
                    isDense: true,
                    style: TextStyle(
                      fontSize: 12,
                      color: context.colors.textSecondary,
                    ),
                    dropdownColor: context.colors.bgElevated,
                    icon: Icon(Icons.arrow_drop_down,
                        size: 16, color: context.colors.textMuted),
                    items: BodyType.values.map((t) {
                      return DropdownMenuItem(
                        value: t,
                        child: Text(_bodyTypeLabel(t),
                            style: TextStyle(
                                fontSize: 12,
                                color: context.colors.textPrimary)),
                      );
                    }).toList(),
                    onChanged: (t) {
                      if (t != null) {
                        ref.read(tabsProvider.notifier).updateRequest(
                            widget.tab.id, req.copyWith(bodyType: t));
                      }
                    },
                  ),
                ),
              ),
            ],
          ),
        ),
        const Divider(height: 1),
        Expanded(
          child: TabBarView(
            controller: _tabCtrl,
            children: [
              Builder(
                builder: (context) {
                  final text = _ctrl.text;
                  final hasVars = text.contains('{{');
                  final resolvedText = (hasVars &&
                          widget.resolvedEnvVars != null)
                      ? VariableParser.resolve(text, widget.resolvedEnvVars!)
                      : null;

                  return Container(
                    color: context.colors.bgBase,
                    decoration: BoxDecoration(
                      color: context.colors.bgBase,
                      border: hasVars
                          ? Border.all(
                              color:
                                  context.colors.warning.withValues(alpha: 0.4),
                              width: 1.0,
                            )
                          : null,
                    ),
                    child: Builder(
                      builder: (context) {
                        Widget editField = VariableAutocompleteField(
                          controller: _ctrl,
                          focusNode: _focus,
                          maxLines: null,
                          expands: true,
                          envVars: widget.envVars ?? [],
                          resolvedEnvVars: widget.resolvedEnvVars,
                          onChanged: (v) {
                            ref.read(tabsProvider.notifier).updateRequest(
                                  widget.tab.id,
                                  widget.tab.request.copyWith(body: v),
                                );
                          },
                          style: TextStyle(
                            fontFamily: 'monospace',
                            fontSize: 13,
                            color: context.colors.textPrimary,
                            height: 1.6,
                          ),
                          decoration: InputDecoration(
                            border: InputBorder.none,
                            enabledBorder: InputBorder.none,
                            focusedBorder: InputBorder.none,
                            contentPadding: const EdgeInsets.all(12),
                            filled: true,
                            fillColor: context.colors.bgBase,
                            hintText: '{\n  "key": "value"\n}',
                          ),
                        );

                        if (resolvedText != null && resolvedText != text) {
                          editField = Tooltip(
                            message: resolvedText,
                            preferBelow: true,
                            decoration: BoxDecoration(
                              color: context.colors.bgElevated,
                              borderRadius: BorderRadius.circular(6),
                              border: Border.all(
                                  color: context.colors.warning
                                      .withValues(alpha: 0.4)),
                            ),
                            textStyle: TextStyle(
                              fontSize: 12,
                              color: context.colors.warning,
                              fontFamily: 'monospace',
                            ),
                            child: editField,
                          );
                        }

                        return editField;
                      },
                    ),
                  );
                },
              ),
              // ── Preview tab: read-only syntax-highlighted view ──────────
              Container(
                color: const Color(0xFF282C34), // atom-one-dark background
                child: Scrollbar(
                  child: SingleChildScrollView(
                    child: SelectionArea(
                      child: CodeViewer(
                        code: _ctrl.text,
                        language: _language,
                      ),
                    ),
                  ),
                ),
              ),
            ],
          ),
        ),
      ],
    );
  }
}

